bool detect_key_down(void) {
    // store the previous cycle's cache
    for (uint8_t i = 0; i < KEYS_SIZE; ++i)
    {
        pressed_keys_prev[i].first = pressed_keys[i].first;
        pressed_keys_prev[i].second = pressed_keys[i].second;
    }

    // fill cache with currently pressed keys
    pressed_keys_index = 0;
    for (uint8_t x = 0; x < MATRIX_ROWS; x++)
    {
        for (uint8_t y = 0; y < MATRIX_COLS; y++)
        {
            // is this key is currently down?
            if (((matrix_get_row(x) & (1 << y)) > 0))
            {
                pressed_keys[pressed_keys_index].first = x+1; // adding 1 to the row/col so that we can use 0 as a null-check
                pressed_keys[pressed_keys_index].second = y+1;
            }
            else
            {
                pressed_keys[pressed_keys_index].first = 0;
                pressed_keys[pressed_keys_index].second = 0;
            }
            pressed_keys_index++;
        }
    }

    // check for a new key down compared to last cycle
    for (uint8_t i = 0; i < KEYS_SIZE; ++i)
    {
        if (pressed_keys[i].first && pressed_keys[i].second && !pressed_keys_prev[i].first && !pressed_keys_prev[i].second)
        {
            return true;
        }
    }
    return false;
}

void eval_anim_state(void) {
    key_down = detect_key_down();

    switch (anim_state)
    {
        case Idle:
            if (key_down) // Idle to Tap
            {
                anim_state = Tap;
            }
            break;

        case Prep:
            if (key_down) // Prep to Tap
            {
                anim_state = Tap;
            }
            else if (timer_elapsed32(idle_timeout_timer) >= IDLE_TIMEOUT) // Prep to Idle
            {
                anim_state = Idle;
                current_idle_frame = 0;
            }
            break;

        case Tap:
            if (!key_down) // Tap to Prep
            {
                anim_state = Prep;
                idle_timeout_timer = timer_read32();
            }
            break;

        default:
            break;
    }
}

static void draw_bongo_or_render_logo(bool minimal) {
    eval_anim_state();

    oled_set_cursor(0, 0);

    if (get_current_wpm() == 0) {
        // The code inside this if condition is a workaround for clearing
        // the bongo animation character. It is a way to prevent the OLED from
        // continuously printing, which I discovered was causing the display to
        // never automatically turn off after being idle for some time. It makes sense
        // that it works this way, as it seems to be an internal code that is not
        // found within this script that controls the OLED screen turning off
        // after a certain amount of idle time. Printing something exactly the
        // same on the OLED translates as not being idle.
        //
        // TODO: Modify the qmk_logo so that it will print a blank space on its
        // right side, allowing the line printed by the bongo character to be
        // deleted. This will eliminate the need for the workaround code.
        // Alternatively, modify the bongo character so that the diagonal
        // printed line is shorter.
        if (!oled_cleared) {
            for (int i = 1; i <= 5; i++) oled_write_ln_P(PSTR(""), false);
            oled_set_cursor(0, 0);
            oled_cleared = true;
        }
        oled_write_P(qmk_logo, false);
    } else {
        oled_cleared = false;
        switch (anim_state) {
            case Idle:
                if (minimal)
                    oled_write_raw_P(idle_minimal[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE);
                else
                    oled_write_raw_P(idle[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE);

                if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION)
                {
                    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                    anim_timer = timer_read32();
                }
                break;

            case Prep:
                if (minimal)
                    oled_write_raw_P(prep_minimal[0], ANIM_SIZE);
                else
                    oled_write_raw_P(prep[0], ANIM_SIZE);
                break;

            case Tap:
                if (minimal)
                    oled_write_raw_P(tap_minimal[abs((TAP_FRAMES - 1) - current_tap_frame)], ANIM_SIZE);
                else
                    oled_write_raw_P(tap[abs((TAP_FRAMES - 1) - current_tap_frame)], ANIM_SIZE);
                current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
                break;

            default:
                break;
        }

        if (!minimal) {
            // print wpm
            oled_set_cursor(0, 0);
            oled_write_P(PSTR("WPM: "), false);
            oled_write(get_u8_str(get_current_wpm(), '0'), false);
        }
    }
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        print_status_narrow();
    } else {
        draw_bongo_or_render_logo(false);
    }
    return false;
}

